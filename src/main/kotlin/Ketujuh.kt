fun main(args: Array<String>) {
    //memakai variabel dinamis, dimana variabel ini tidak memerlukan deklarasi tipe
    var dynamicVar = 2
    println("Contoh dynamic variabel = " + dynamicVar)

    //memakai variabel statis, kebalikan dari variabel dinamis
    var staticVar: Char = 'A'
    println("Contoh static variabel = " + staticVar)
}